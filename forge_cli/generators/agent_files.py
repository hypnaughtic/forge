"""Generate agent instruction files for .claude/agents/ directory."""

from __future__ import annotations

from pathlib import Path
from textwrap import dedent

from forge_cli.config_schema import ForgeConfig


def generate_agent_files(config: ForgeConfig, agents_dir: Path) -> None:
    """Generate all agent instruction files."""
    active_agents = config.get_active_agents()

    for agent_type in active_agents:
        content = _build_agent_file(agent_type, config)
        output_path = agents_dir / f"{agent_type}.md"
        output_path.write_text(content)


def _build_agent_file(agent_type: str, config: ForgeConfig) -> str:
    """Build a complete agent instruction file."""
    sections: list[str] = []

    # Header
    sections.append(f"<!-- Generated by Forge v3.0 — Regenerate with: forge --config forge-config.yaml -->")
    sections.append("")

    # Project context
    sections.append(_project_context_section(config))

    # Agent-specific instructions
    agent_instructions = AGENT_TEMPLATES.get(agent_type)
    if agent_instructions:
        sections.append(agent_instructions(config))
    else:
        sections.append(f"# {agent_type.replace('-', ' ').title()}\n\nCustom agent. Follow team leader instructions.")

    # Non-negotiables (injected between agent template and base protocol)
    if config.non_negotiables:
        sections.append(_non_negotiables_section(agent_type, config))

    # Base protocol (embedded, not referenced)
    sections.append(_base_protocol_section(config))

    # Workflow enforcement protocol (always included, adapts to config)
    sections.append(_workflow_enforcement_section(agent_type, config))

    # Sub-agent spawning protocol (if enabled)
    if config.agents.allow_sub_agent_spawning:
        sections.append(_sub_agent_spawning_section(agent_type, config))

    # Agent naming protocol
    if config.agent_naming.enabled:
        sections.append(_agent_naming_section(config))

    # Atlassian integration (if enabled)
    if config.atlassian.enabled:
        sections.append(_atlassian_section(agent_type, config))

    # Visual verification protocol
    # Always-on for frontend specialists and QA (they inherently work on UI)
    # Conditional on frontend involvement for team-leader and critic (review roles)
    always_visual = {"frontend-engineer", "frontend-developer", "frontend-designer", "qa-engineer"}
    review_visual = {"team-leader", "critic"}
    if agent_type in always_visual:
        sections.append(_visual_verification_section(agent_type, config))
    elif agent_type in review_visual and config.has_frontend_involvement():
        sections.append(_visual_verification_section(agent_type, config))

    # LLM Gateway integration (if enabled)
    if config.llm_gateway.enabled:
        sections.append(_llm_gateway_section(agent_type, config))

    # Workspace detection
    sections.append(_workspace_detection_section())

    # Custom per-agent instructions
    custom = config.agents.custom_instructions.get(agent_type, "")
    if custom:
        sections.append(f"## Custom Instructions (User-Specified)\n\n{custom}")

    return "\n\n---\n\n".join(sections)


def _non_negotiables_section(agent_type: str, config: ForgeConfig) -> str:
    """Generate non-negotiables section with role-appropriate framing."""
    rules = "\n".join(f"- {rule}" for rule in config.non_negotiables)

    if agent_type == "team-leader":
        return dedent(f"""\
        ## Non-Negotiable Requirements (ENFORCEMENT)

        The following requirements are ABSOLUTE. No exceptions. No trade-offs.

        {rules}

        **Your responsibility as Team Leader:**
        - Reject any work that violates these requirements
        - Include a Non-Negotiable Compliance check in every iteration review
        - Communicate these to every agent during spawning
        """)
    elif agent_type == "critic":
        return dedent(f"""\
        ## Non-Negotiable Requirements (EVALUATION)

        The following requirements are ABSOLUTE. Every critique MUST include a
        Non-Negotiable Compliance section that scores each rule as PASS/FAIL.
        Any FAIL is an automatic BLOCKER — the deliverable cannot proceed.

        {rules}
        """)
    else:
        return dedent(f"""\
        ## Non-Negotiable Requirements (COMPLIANCE)

        The following requirements are ABSOLUTE. Your work MUST comply with
        every rule listed below. Verify compliance before reporting work as complete.

        {rules}
        """)


def _project_context_section(config: ForgeConfig) -> str:
    tech_parts = []
    if config.tech_stack.languages:
        tech_parts.append(f"Languages: {', '.join(config.tech_stack.languages)}")
    if config.tech_stack.frameworks:
        tech_parts.append(f"Frameworks: {', '.join(config.tech_stack.frameworks)}")
    if config.tech_stack.databases:
        tech_parts.append(f"Databases: {', '.join(config.tech_stack.databases)}")
    tech_str = " | ".join(tech_parts) if tech_parts else "Auto-detect based on requirements"

    agents = config.get_active_agents()

    return dedent(f"""\
    ## Project Context

    - **Project**: {config.project.description}
    - **Mode**: {config.mode.value}
    - **Strategy**: {config.strategy.value}
    - **Team Profile**: {config.resolve_team_profile()}
    - **Tech Stack**: {tech_str}
    - **Cost Cap**: ${config.cost.max_development_cost}
    - **Active Team**: {', '.join(agents)}
    - **Sub-agent Spawning**: {'Enabled' if config.agents.allow_sub_agent_spawning else 'Disabled'}
    - **Atlassian Integration**: {'Enabled' if config.atlassian.enabled else 'Disabled'}
    - **LLM Gateway**: {'Enabled (local_claude: ' + ('on' if config.llm_gateway.enable_local_claude else 'off') + ')' if config.llm_gateway.enabled else 'Disabled'}

    ### Project Requirements

    {config.project.requirements or 'See project description above.'}""")


def _base_protocol_section(config: ForgeConfig) -> str:
    return dedent("""\
    ## Base Agent Protocol

    > These rules apply to ALL agents. Your role section defines WHAT you do; this section defines HOW you operate.

    ### Communication Protocol

    - Receive tasks via Claude Code Agent Teams task assignment from the Team Leader
    - Report completion via task status updates and direct messaging
    - Peer communication via Agent Teams messaging
    - Include structured metadata in all communications: priority (`normal|high|critical`), type (`status-update|request|response|blocker|deliverable|review-request|review-response|dependency-change`), confidence (`high|medium|low`)

    ### Git Workflow

    - **Commits**: `[{agent-name}] {type}: {description}` — types: feat, fix, refactor, test, docs, ci, chore
    - **Branch naming and PR workflow**: See Workflow Enforcement Protocol below
    - After each verified iteration: `git tag iteration-{N}-verified`

    ### Confidence Signaling

    Every deliverable and status update to Team Leader includes `confidence: high|medium|low`. Add `confidence_note` when medium/low.
    - **high** → standard review
    - **medium** → specialist review requested
    - **low** → mandatory 2+ agent review; escalated to human in Co-Pilot/Micro-Manage

    ### Output Verification Mandate

    **Every agent that produces runnable code must verify it actually runs.** Passing unit tests is necessary but NOT sufficient. Before marking any implementation task as done:

    1. **Start the application** and confirm it starts without errors
    2. **Exercise the feature** you built — send a real request, trigger the real workflow
    3. **Check the output** — verify the response/result is correct

    If you cannot verify (dependency on unfinished work), state this with `confidence: medium`.

    ### Vendor-Agnostic Coding Mandate

    All external dependencies (databases, caches, cloud services, LLM providers, auth, payments, etc.) MUST be behind abstract interfaces with pluggable implementations. Easy vendor switching is non-negotiable.

    ### Secret Safety

    1. NEVER log secrets — reference by env var name only
    2. NEVER include secrets in messages
    3. NEVER commit secrets — `.env` in `.gitignore`; only `.env.example` with placeholders
    4. NEVER put secrets in working memory
    5. Use `.env.example` pattern for all required env vars
    6. Audit for leaks in logs, error messages, API responses, git history

    ### Error Handling

    Unrecoverable errors → notify Team Leader with error details, what you were doing, and what you tried → wait for instructions.

    ### Code Review Protocol

    Submit review requests with: scope, files to review, context, specific concerns, branch name.
    - **BLOCKER**: must fix, re-review
    - **WARNING**: should fix, Team Leader decides
    - **NOTE**: optional suggestion
    Max 2 review rounds — unresolved after 2 rounds, Team Leader decides.""")


def _workflow_enforcement_section(agent_type: str, config: ForgeConfig) -> str:
    """Generate workflow enforcement protocol — branch naming, PR workflow, review quality, release management."""
    if config.atlassian.enabled:
        project_key = config.atlassian.jira_project_key or "PROJ"
        branch_naming = dedent(f"""\
        ### Branch Naming Convention

        Format: `<type>-<JIRA-KEY>-<hyphen-separated-description>`
        - Example: `feat-{project_key}-42-add-user-authentication`
        - Example: `fix-{project_key}-99-handle-null-response`
        - Types: `feat`, `fix`, `chore`, `test`, `refactor`, `docs`, `ci`, `perf`
        - Every branch maps 1:1 to a Jira ticket — no branch without a ticket""")
    else:
        branch_naming = dedent("""\
        ### Branch Naming Convention

        Format: `<type>/<agent-name>/<task-id>-<short-description>`
        - Example: `feat/backend-dev/T1-add-user-auth`
        - Example: `fix/frontend-eng/T5-fix-login-redirect`
        - Types: `feat`, `fix`, `chore`, `test`, `refactor`, `docs`, `ci`, `perf`
        - Every branch maps 1:1 to a unique task""")

    pr_workflow = dedent("""\
    ### Hierarchical PR Workflow

    **No direct merges to the default branch** — all code changes go through Pull Requests.

    - At least one approval required from the lead agent (sub-team leader or Team Leader)
    - Sub-task branches PR into parent feature branch, reviewed by the feature lead
    - Feature branches PR into the default branch, reviewed by the Team Leader
    - Delete branches after merge
    - Non-code tickets (docs updates, process tasks) don't require PRs

    ### PR Review Quality Standards

    - **Big PRs** (new features, cross-cutting changes): reviewer must actually test — run the app, exercise the feature, judge quality against expectations, not just check if it compiles
    - **Straightforward PRs** (docs, config, small fixes): code review is sufficient
    - Reviewer signs off with agent name and confidence level""")

    release_section = "### Release Management\n\n"
    release_section += "- After major merges into the default branch: create a GitHub release with a version tag\n"
    release_section += "- Generate release notes summarizing changes since the last release\n"
    if config.atlassian.enabled:
        release_section += "- Update the Confluence release notes page with the release summary\n"
    release_section += "- Documentation Specialist or Team Leader handles release artifacts"

    return dedent("""\
    ## Workflow Enforcement Protocol

    > These rules govern branching, PRs, reviews, and releases. All agents must follow them.

    """) + branch_naming + "\n\n" + pr_workflow + "\n\n" + release_section


def _sub_agent_spawning_section(agent_type: str, config: ForgeConfig) -> str:
    if config.atlassian.enabled:
        jira_note = "Jira Tickets"
        jira_instruction = "Create Jira sub-tasks for each piece of work"
    else:
        jira_note = "Task Tracking"
        jira_instruction = "Document sub-tasks clearly with IDs and descriptions"

    mode_quality_map = {
        "mvp": "MVP — 70% threshold, speed over polish",
        "production-ready": "Production Ready — 90% threshold, robust and maintainable",
        "no-compromise": "No Compromise — 100% threshold, maximum quality",
    }
    mode_quality_text = mode_quality_map.get(config.mode.value, config.mode.value)

    return dedent(f"""\
    ## Sub-Agent Spawning Protocol

    You are authorized to spawn sub-agents to parallelize your work. This is a powerful capability — use it wisely.

    ### When to Spawn Sub-Agents

    - Your assigned task can be decomposed into **independent** sub-tasks that benefit from parallel execution
    - Example: You're assigned to build an entire frontend — spawn sub-agents for independent pages/features
    - Example: A backend API change requires checking frontend integration impacts — spawn a frontend sub-agent
    - Example: Multiple independent test suites need to run — spawn sub-agents per suite

    ### How to Spawn

    1. Use the **Agent tool** in Claude Code to spawn a sub-agent
    2. Load the appropriate agent instruction file from `.claude/agents/{{agent-type}}.md` as the sub-agent's context
    3. Provide a clear, scoped task description in the spawn prompt
    4. The sub-agent inherits all protocols from its instruction file (including this spawning protocol)

    ### Rules

    - Sub-agents MUST use instruction files from `.claude/agents/` — never spawn a bare agent without forge instructions
    - Sub-agents MUST follow the same git workflow: branch naming, commit format, confidence signaling
    - Sub-agents report back to YOU (the spawning agent), not directly to Team Leader
    - YOU are responsible for aggregating sub-agent results and reporting to Team Leader
    - Sub-agents can spawn their own sub-agents if the task warrants it (recursive spawning)
    - Keep spawning depth reasonable — 2-3 levels max
    - Each sub-agent must identify itself with a unique name (see Agent Naming Protocol)

    ### Cross-Specialty Spawning

    You may spawn sub-agents of a DIFFERENT specialty when needed:
    - Backend developer changes an API → spawn a frontend sub-agent to check integration impacts
    - Frontend engineer needs a quick API endpoint → spawn a backend sub-agent
    - Any agent can spawn a QA sub-agent to validate their own output

    Always load the correct specialty's instruction file from `.claude/agents/` for cross-specialty spawns.

    ### Leadership Responsibilities (You Are a Team Leader)

    When you spawn sub-agents, you become the **leader of your micro-team**. This is not optional:

    1. **Task Decomposition**: Break your task into clear sub-tasks with acceptance criteria
    2. **{jira_note}**: {jira_instruction}
    3. **Feature Branch**: Create a parent feature branch; sub-agents branch off it
    4. **Assignment**: Assign sub-tasks to sub-agents with full context
    5. **Progress Monitoring**: Check sub-agent status regularly, unblock them
    6. **PR Review**: You are the reviewer for all PRs from your sub-agents (at least one approval required)
    7. **Quality Gates**: Review sub-agent deliverables before integrating into your work
    8. **Integration**: Merge approved sub-agent PRs into your feature branch, resolve conflicts
    9. **Reporting**: Aggregate results and report to YOUR leader (Team Leader or parent agent)
    10. **Accountability**: You are responsible for your sub-agents' output quality

    Apply the same rigor the Team Leader applies to you.

    ### Mandatory Sub-Team Critic

    When you form a micro-team (you + sub-agents), you MUST spawn a **Critic sub-agent**:

    1. **Spawn the Critic**: Use `.claude/agents/critic.md` as the instruction file
    2. **Provide Context**: Give the Critic:
       - Project requirements (from your instruction file's Project Context)
       - Your specific task scope and acceptance criteria
       - Clear KPIs and quality gates for the sub-team's deliverables
       - Test cases or expected outcomes to judge against
    3. **Critic Reviews Before Integration**: Sub-agent deliverables pass through the sub-team Critic BEFORE you merge them
    4. **Big Picture Awareness**: The Critic evaluates against:
       - Overall project requirements and user preferences (not just the immediate sub-task)
       - Broader system architecture and integration points
       - Quality mode standards ({mode_quality_text})
       - Real-world usability — would an actual user find this valuable and usable?
       - Pragmatic balance — perfection vs. shipping, but never ship broken code
    5. **Critic Reports to You**: The sub-team Critic reports to you (the sub-team leader), not to the main Team Leader
    6. **Escalation**: Issues affecting the broader project get escalated to the main Team Leader

    This is crucial for long/complex projects. Without independent review at every layer, quality degrades as teams scale.""")


